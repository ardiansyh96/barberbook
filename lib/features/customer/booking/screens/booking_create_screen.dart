/// Booking Creation screen with multi-step wizard flow.
///
/// Steps:
/// 1. Select Barber (from Firestore active barbers)
/// 2. Select Service (from Firestore active services)
/// 3. Select Date (calendar picker, no past dates, max 30 days)
/// 4. Select Time (available time slots based on barber working hours)
/// 5. Add Notes (optional)
/// 6. Review & Confirm (summary with total price)
///
/// Features:
/// - Accepts optional `barberId` query parameter to pre-select barber
/// - Validates each step before proceeding
/// - Creates booking in Firestore with denormalized display fields
/// - Navigates to booking history on success
class BookingCreateScreen extends ConsumerStatefulWidget {
  /// Optional pre-selected barber ID (from barber detail "Book Now")
  final String? initialBarberId;

  const BookingCreateScreen({super.key, this.initialBarberId});

  @override
  ConsumerState<BookingCreateScreen> createState() => _BookingCreateScreenState();
}

class _BookingCreateScreenState extends ConsumerState<BookingCreateScreen> {
  int _currentStep = 0;
  bool _isSubmitting = false;

  // Selected values
  BarberModel? _selectedBarber;
  ServiceModel? _selectedService;
  DateTime? _selectedDate;
  String? _selectedTime;
  final _notesController = TextEditingController();

  @override
  void initState() {
    super.initState();
    // Pre-select barber if barberId is provided
    if (widget.initialBarberId != null && widget.initialBarberId!.isNotEmpty) {
      _loadInitialBarber(widget.initialBarberId!);
    }
  }

  @override
  void dispose() {
    _notesController.dispose();
    super.dispose();
  }

  /// Load barber data when initialBarberId is provided
  void _loadInitialBarber(String barberId) {
    WidgetsBinding.instance.addPostFrameCallback((_) async {
      final barber = await ref.read(barberByIdProvider(barberId).future);
      if (barber != null && mounted) {
        setState(() {
          _selectedBarber = barber;
          if (_currentStep == 0) _currentStep = 1; // Skip to service selection
        });
      }
    });
  }

  /// Maximum bookable date (30 days from today)
  DateTime get _maxDate =>
      DateTime.now().add(const Duration(days: AppConstants.maxBookingDaysAhead));

  /// Minimum bookable date (today)
  DateTime get _minDate {
    final now = DateTime.now();
    return DateTime(now.year, now.month, now.day);
  }

  /// Create the booking in Firestore
  Future<void> _handleCreateBooking() async {
    if (_selectedBarber == null ||
        _selectedService == null ||
        _selectedDate == null ||
        _selectedTime == null) {
      return;
    }

    setState(() => _isSubmitting = true);

    final user = ref.read(authStateProvider).whenOrNull(data: (u) => u);
    if (user == null) {
      setState(() => _isSubmitting = false);
      SnackbarHelper.error(context, 'You must be logged in to book');
      return;
    }

    try {
      final booking = BookingModel(
        id: '', // Will be generated by Firestore
        customerId: user.uid,
        barberId: _selectedBarber!.id,
        serviceId: _selectedService!.id,
        tanggal: _selectedDate!,
        jam: _selectedTime!,
        status: AppConstants.statusPending,
        catatan: _notesController.text.trim().isEmpty
            ? null
            : _notesController.text.trim(),
        totalHarga: _selectedService!.harga,
        createdAt: DateTime.now(),
        // Denormalized fields for display
        customerNama: user.nama,
        barberNama: _selectedBarber!.nama,
        serviceNama: _selectedService!.nama,
        barberFoto: _selectedBarber!.foto,
      );

      await ref.read(bookingServiceProvider).createBooking(booking);

      if (!mounted) return;

      SnackbarHelper.success(context, 'Booking created successfully!');
      context.go(RouteNames.customerBooking);
    } catch (e) {
      if (!mounted) return;
      setState(() => _isSubmitting = false);
      SnackbarHelper.error(context, e.toString());
    }
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      backgroundColor: AppColors.backgroundLight,
      appBar: AppBar(
        title: const Text('Book Appointment'),
        leading: IconButton(
          icon: const Icon(Icons.arrow_back_ios),
          onPressed: () => context.pop(),
        ),
      ),
      body: Column(
        children: [
          // Step indicator
          _buildStepIndicator(),

          // Step content
          Expanded(
            child: AnimatedSwitcher(
              duration: const Duration(milliseconds: 300),
              child: _buildCurrentStep(),
            ),
          ),
        ],
      ),
      // Bottom navigation buttons
      bottomNavigationBar: _buildBottomBar(),
    );
  }

  // ─── Step Indicator ───────────────────────────────────────────────
  Widget _buildStepIndicator() {
    final steps = ['Barber', 'Service', 'Date', 'Time', 'Notes'];

    return Container(
      padding: const EdgeInsets.symmetric(
        horizontal: AppDimensions.spacingXL,
        vertical: AppDimensions.spacingMD,
      ),
      color: AppColors.white,
      child: Row(
        children: List.generate(steps.length, (index) {
          final isActive = index <= _currentStep;
          final isCompleted = index < _currentStep;

          return Expanded(
            child: Column(
              children: [
                Container(
                  width: 28,
                  height: 28,
                  decoration: BoxDecoration(
                    color: isActive ? AppColors.primaryBlack : AppColors.lightGrey,
                    shape: BoxShape.circle,
                  ),
                  child: Center(
                    child: isCompleted
                        ? const Icon(Icons.check, color: Colors.white, size: 16)
                        : Text(
                            '${index + 1}',
                            style: TextStyle(
                              color: isActive ? Colors.white : AppColors.darkGrey,
                              fontSize: 12,
                              fontWeight: FontWeight.w600,
                            ),
                          ),
                  ),
                ),
                const SizedBox(height: 4),
                Text(
                  steps[index],
                  style: TextStyle(
                    fontSize: 10,
                    color: isActive ? AppColors.primaryBlack : AppColors.darkGrey,
                    fontWeight: isActive ? FontWeight.w600 : FontWeight.normal,
                  ),
                ),
              ],
            ),
          );
        }),
      ),
    );
  }

  // ─── Current Step Content ─────────────────────────────────────────
  Widget _buildCurrentStep() {
    switch (_currentStep) {
      case 0:
        return _buildSelectBarber();
      case 1:
        return _buildSelectService();
      case 2:
        return _buildSelectDate();
      case 3:
        return _buildSelectTime();
      case 4:
        return _buildReviewAndConfirm();
      default:
        return _buildSelectBarber();
    }
  }

  // ─── Step 0: Select Barber ────────────────────────────────────────
  Widget _buildSelectBarber() {
    final barbersAsync = ref.watch(activeBarbersProvider);

    return barbersAsync.when(
      loading: () => const Center(child: CircularProgressIndicator()),
      error: (_, _) => const Center(child: Text('Failed to load barbers')),
      data: (barbers) {
        if (barbers.isEmpty) {
          return const Center(child: Text('No barbers available'));
        }
        return ListView.builder(
          padding: const EdgeInsets.all(AppDimensions.spacingXL),
          itemCount: barbers.length,
          itemBuilder: (context, index) {
            final barber = barbers[index];
            final isSelected = _selectedBarber?.id == barber.id;

            return GestureDetector(
              onTap: () => setState(() => _selectedBarber = barber),
              child: Container(
                margin: const EdgeInsets.only(bottom: AppDimensions.spacingMD),
                padding: const EdgeInsets.all(AppDimensions.spacingMD),
                decoration: BoxDecoration(
                  color: AppColors.white,
                  borderRadius: BorderRadius.circular(AppDimensions.radiusLG),
                  border: Border.all(
                    color: isSelected
                        ? AppColors.gold
                        : AppColors.lightGrey,
                    width: isSelected ? 2 : 1,
                  ),
                  boxShadow: isSelected ? AppDimensions.shadowMD : AppDimensions.shadowSM,
                ),
                child: Row(
                  children: [
                    // Photo
                    barber.foto != null
                        ? CachedImage(
                            imageUrl: barber.foto!,
                            width: 56,
                            height: 56,
                            borderRadius: 28,
                            fit: BoxFit.cover,
                          )
                        : CircleAvatar(
                            radius: 28,
                            backgroundColor: AppColors.lightGrey,
                            child: const Icon(Icons.person, color: AppColors.mediumGrey),
                          ),
                    const SizedBox(width: AppDimensions.spacingMD),
                    // Info
                    Expanded(
                      child: Column(
                        crossAxisAlignment: CrossAxisAlignment.start,
                        children: [
                          Text(
                            barber.nama,
                            style: const TextStyle(
                              fontWeight: FontWeight.w700,
                              fontSize: 16,
                            ),
                          ),
                          Text(
                            barber.spesialis,
                            style: TextStyle(color: AppColors.darkGrey, fontSize: 13),
                          ),
                          const SizedBox(height: 4),
                          Row(
                            children: [
                              const Icon(Icons.star, color: AppColors.starFilled, size: 14),
                              const SizedBox(width: 2),
                              Text(
                                Formatters.rating(barber.rating),
                                style: const TextStyle(fontWeight: FontWeight.w600, fontSize: 12),
                              ),
                              const SizedBox(width: AppDimensions.spacingSM),
                              Text(
                                '${barber.pengalaman} years exp',
                                style: TextStyle(color: AppColors.darkGrey, fontSize: 12),
                              ),
                            ],
                          ),
                        ],
                      ),
                    ),
                    // Check icon
                    if (isSelected)
                      Container(
                        width: 24,
                        height: 24,
                        decoration: const BoxDecoration(
                          color: AppColors.successGreen,
                          shape: BoxShape.circle,
                        ),
                        child: const Icon(Icons.check, color: Colors.white, size: 16),
                      ),
                  ],
                ),
              ),
            );
          },
        );
      },
    );
  }

