  }

  /// Open date picker dialog
  Future<void> _pickDate() async {
    final picked = await showDatePicker(
      context: context,
      initialDate: _selectedDate ?? _minDate,
      firstDate: _minDate,
      lastDate: _maxDate,
      builder: (context, child) {
        return Theme(
          data: Theme.of(context).copyWith(
            colorScheme: const ColorScheme.light(
              primary: AppColors.primaryBlack,
              onPrimary: Colors.white,
              onSurface: AppColors.charcoal,
            ),
          ),
          child: child!,
        );
      },
    );
    if (picked != null) {
      setState(() {
        _selectedDate = picked;
        _selectedTime = null; // Reset time when date changes
      });
    }
  }

  /// Get day name from DateTime
  String _getDayName(DateTime date) {
    const days = ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday', 'Sunday'];
    return days[date.weekday - 1];
  }

  // ─── Step 3: Select Time ──────────────────────────────────────────
  Widget _buildSelectTime() {
    if (_selectedBarber == null || _selectedDate == null) {
      return const Center(child: Text('Please select barber and date first'));
    }

    final slotsAsync = ref.watch(
      availableTimeSlotsProvider((
        barberId: _selectedBarber!.id,
        tanggal: _selectedDate!,
        jamMasuk: _selectedBarber!.jamMasuk,
        jamPulang: _selectedBarber!.jamPulang,
      )),
    );

    return Padding(
      padding: const EdgeInsets.all(AppDimensions.spacingXL),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            'Select Time',
            style: Theme.of(context).textTheme.titleLarge,
          ),
          const SizedBox(height: AppDimensions.spacingSM),
          Text(
            'Available time slots for ${_selectedBarber!.nama}',
            style: TextStyle(color: AppColors.darkGrey, fontSize: 14),
          ),
          const SizedBox(height: AppDimensions.spacingLG),
          Expanded(
            child: slotsAsync.when(
              loading: () => const Center(child: CircularProgressIndicator()),
              error: (_, _) => const Center(child: Text('Failed to load time slots')),
              data: (slots) {
                if (slots.isEmpty) {
                  return const Center(
                    child: Text('No available time slots for this date'),
                  );
                }
                return GridView.builder(
                  gridDelegate: const SliverGridDelegateWithFixedCrossAxisCount(
                    crossAxisCount: 3,
                    crossAxisSpacing: 8,
                    mainAxisSpacing: 8,
                    childAspectRatio: 2.5,
                  ),
                  itemCount: slots.length,
                  itemBuilder: (context, index) {
                    final slot = slots[index];
                    final isSelected = _selectedTime == slot;

                    return GestureDetector(
                      onTap: () => setState(() => _selectedTime = slot),
                      child: Container(
                        decoration: BoxDecoration(
                          color: isSelected
                              ? AppColors.primaryBlack
                              : AppColors.white,
                          borderRadius: BorderRadius.circular(AppDimensions.radiusMD),
                          border: Border.all(
                            color: isSelected
                                ? AppColors.primaryBlack
                                : AppColors.lightGrey,
                          ),
                        ),
                        child: Center(
                          child: Text(
                            Formatters.time12h(slot),
                            style: TextStyle(
                              color: isSelected ? Colors.white : AppColors.charcoal,
                              fontWeight: FontWeight.w600,
                              fontSize: 14,
                            ),
                          ),
                        ),
                      ),
                    );
                  },
                );
              },
            ),
          ),
        ],
      ),
    );
  }

  // ─── Step 4: Review & Confirm ─────────────────────────────────────
  Widget _buildReviewAndConfirm() {
    return SingleChildScrollView(
      padding: const EdgeInsets.all(AppDimensions.spacingXL),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            'Review Booking',
            style: Theme.of(context).textTheme.titleLarge,
          ),
          const SizedBox(height: AppDimensions.spacingSM),
          Text(
            'Please review your booking details',
            style: TextStyle(color: AppColors.darkGrey, fontSize: 14),
          ),
          const SizedBox(height: AppDimensions.spacingLG),

          // Summary card
          Container(
            padding: const EdgeInsets.all(AppDimensions.spacingLG),
            decoration: BoxDecoration(
              color: AppColors.white,
              borderRadius: BorderRadius.circular(AppDimensions.radiusLG),
              boxShadow: AppDimensions.shadowMD,
            ),
            child: Column(
              children: [
                // Barber
                _buildSummaryRow(
                  icon: Icons.person,
                  label: 'Barber',
                  value: _selectedBarber?.nama ?? '-',
                ),
                const Divider(height: 24),

                // Service
                _buildSummaryRow(
                  icon: Icons.content_cut,
                  label: 'Service',
