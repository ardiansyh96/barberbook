  // ─── Bottom Navigation Bar ────────────────────────────────────────
  Widget _buildBottomBar() {
    return Container(
      padding: const EdgeInsets.fromLTRB(
        AppDimensions.spacingXL,
        AppDimensions.spacingMD,
        AppDimensions.spacingXL,
        AppDimensions.spacingXL,
      ),
      decoration: BoxDecoration(
        color: AppColors.white,
        boxShadow: [
          BoxShadow(
            color: Colors.black.withValues(alpha: 0.05),
            blurRadius: 10,
            offset: const Offset(0, -2),
          ),
        ],
      ),
      child: Row(
        children: [
          // Back button (hidden on first step)
          if (_currentStep > 0)
            Padding(
              padding: const EdgeInsets.only(right: AppDimensions.spacingMD),
              child: OutlinedButton(
                onPressed: _isSubmitting ? null : () => setState(() => _currentStep--),
                style: OutlinedButton.styleFrom(
                  padding: const EdgeInsets.symmetric(horizontal: 20, vertical: 14),
                  side: const BorderSide(color: AppColors.lightGrey),
                ),
                child: const Text('Back'),
              ),
            ),

          // Next/Confirm button
          Expanded(
            child: SizedBox(
              height: AppDimensions.buttonHeightLG,
              child: ElevatedButton(
                onPressed: _canProceed()
                    ? (_currentStep == 4 ? _handleCreateBooking : _nextStep)
                    : null,
                style: ElevatedButton.styleFrom(
                  backgroundColor: AppColors.primaryBlack,
                  foregroundColor: AppColors.white,
                  disabledBackgroundColor: AppColors.lightGrey,
                ),
                child: _isSubmitting
                    ? const SizedBox(
                        width: 24,
                        height: 24,
                        child: CircularProgressIndicator(
                          color: AppColors.gold,
                          strokeWidth: 2,
                        ),
                      )
                    : Text(
                        _currentStep == 4 ? 'Confirm Booking' : 'Next',
                        style: const TextStyle(
                          fontSize: 16,
                          fontWeight: FontWeight.w700,
                        ),
                      ),
              ),
            ),
          ),
        ],
      ),
    );
  }

  /// Check if the user can proceed to the next step
  bool _canProceed() {
    if (_isSubmitting) return false;
    switch (_currentStep) {
      case 0:
        return _selectedBarber != null;
      case 1:
        return _selectedService != null;
      case 2:
        return _selectedDate != null;
      case 3:
        return _selectedTime != null;
      case 4:
        return true;
      default:
        return false;
    }
  }

  /// Move to the next step
  void _nextStep() {
    if (_canProceed() && _currentStep < 4) {
      setState(() => _currentStep++);
    }
  }
}
